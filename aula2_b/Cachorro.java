package aula2_b;

public class Cachorro {

    private String nome, corDoPelo;
    private int idade, quantVida, forcaDaMordida;

    public void setNome(String nome) {
        this.nome = nome;
    }
    public String getNome() {
        return nome;
    }
    
    public void setCorDoPelo(String corDoPelo) {
        this.corDoPelo = corDoPelo;
    }
    public String getCorDoPelo() {
        return corDoPelo;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }
    public int getIdade() {
        return idade;
    }
    
    public void setQuantVida(int quantVida) {
        this.quantVida = quantVida;
    }
    public int getQuantVida() {
        return quantVida;
    }
    
    public void setForcaDaMordida(int forcaDaMordida) {
        this.forcaDaMordida = forcaDaMordida;
    }
    public int getForcaDaMordida() {
        return forcaDaMordida;
    }

}
