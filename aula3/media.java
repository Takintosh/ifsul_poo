package aula3;

public class media {
    String titulo;
    int duracion;

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }
    public void setDuracion(int duracion) {
        this.duracion = duracion;
    }
    
    public String getTitulo() {
        return titulo;
    }
    public int getDuracion() {
        return duracion;
    }

}
