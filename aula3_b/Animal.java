package aula3_b;

public class Animal {

    private String cor, numeroPatas;
    private int numeroIndividuos;

    public void setCor(String cor) {
        this.cor = cor;
    }
    public String  getCor() {
        return cor;
    }

    public void setNumeroPatas(String numeroPatas) {
        this.numeroPatas = numeroPatas;
    }
    public String  getNumeroPatas() {
        return numeroPatas;
    }

    public void setNumeroIndividuos(int numeroIndividuos) {
        this.numeroIndividuos = numeroIndividuos;
    }
    public int getNumeroIndividuos() {
        return numeroIndividuos;
    }

}
