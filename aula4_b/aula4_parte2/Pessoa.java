package aula4_b.aula4_parte2;

public class Pessoa {
    
    private String nome;
    private int nroConta;

    public void setNome(String nome) {
        this.nome = nome;
    }
    public String getNome() {
        return nome;
    }

    public void setNroConta(int nroConta) {
        this.nroConta = nroConta;
    }
    public int getNroConta() {
        return nroConta;
    }

}
